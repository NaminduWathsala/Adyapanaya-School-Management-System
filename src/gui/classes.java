    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String te = jLabel13.getText();
        String sub = jLabel19.getText();
        String timeSlot = jComboBox2.getSelectedItem().toString();

        if (te.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Teacher", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (timeSlot.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select the Timeslot", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (sub.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Subject", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                MySQL.iud("INSERT INTO `Class` (`Subno`,`Tno`,`timeslot`) VALUES ('" + sub + "','" + te + "','" + timeSlot + "') ");

                loadTeachers();
                resetFields();

                JOptionPane.showMessageDialog(this, "New  Class added", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

