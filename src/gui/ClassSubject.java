    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable3.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a Student", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                String Subno = jTable3.getValueAt(r, 0).toString();
                String price = jTable3.getValueAt(r, 1).toString();
                String description = jTable3.getValueAt(r, 2).toString();

                if (inc == null) {

                    cl.jLabel19.setText(Subno);
                    cl.jLabel21.setText(price);
                    cl.jLabel23.setText(description);
                } else {
                    inc.jLabel19.setText(Subno);
                    inc.jLabel21.setText(price);
                    inc.jLabel23.setText(description);
                }
                this.dispose();

            }

        }
    }//GEN-LAST:event_jTable3MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String price = jTextField1.getText();
        String description = jTextArea1.getText();

        if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Price", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (description.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Description", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                MySQL.iud("INSERT INTO `Subject` (`Description`,`Price`) VALUES ('" + description + "','" + price + "') ");

                loadSubject();
                resetFields();

                JOptionPane.showMessageDialog(this, "New  Subject added", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

