    public void loadStudents() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `Student` ORDER BY `Student`.`Sno` ASC ;");

            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("Sno"));
                v.add(rs.getString("Name"));
                v.add(rs.getString("Address"));
                v.add(rs.getString("dob"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadStudents(String text) {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `Student` WHERE `Student`.`Sno` LIKE '" + text + "%' OR `Student`.`Name` LIKE '" + text + "%' OR `Student`.`Address` LIKE '" + text + "%' OR `Student`.`dob` LIKE '" + text + "%' ORDER BY `Student`.`Sno` ;");

            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("Sno"));
                v.add(rs.getString("Name"));
                v.add(rs.getString("Address"));
                v.add(rs.getString("dob"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

