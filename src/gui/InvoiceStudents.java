    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable3.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a Student", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                String sid = jTable3.getValueAt(r, 0).toString();
                String name = jTable3.getValueAt(r, 1).toString();
                String dob = jTable3.getValueAt(r, 3).toString();

                inc.jLabel24.setText(sid);
                inc.jLabel26.setText(name);
                inc.jLabel28.setText(dob);

                this.dispose();

            }

        }
    }//GEN-LAST:event_jTable3MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String fn = jTextField1.getText();
        Date dob = jDateChooser1.getDate();
        String address = jTextArea1.getText();

        if (fn.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter First Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (dob == null) {
            JOptionPane.showMessageDialog(this, "Please enter Birth date", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

                MySQL.iud("INSERT INTO `Student` (`Name`,`Address`,`dob`) VALUES ('" + fn + "','" + address + "','" + sdf.format(dob) + "') ");

                loadStudents();
                resetFields();

                JOptionPane.showMessageDialog(this, "New  Student added", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

