    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String fn = jTextField1.getText();
        String suject = jTextField4.getText();
        String address = jTextArea1.getText();

        if (fn.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter First Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (suject.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Subject", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                MySQL.iud("INSERT INTO `Teacher` (`Name`,`Address`,`subjects`) VALUES ('" + fn + "','" + address + "','" + suject + "') ");

                loadTeachers();
                resetFields();

                JOptionPane.showMessageDialog(this, "New  Teacher added", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable3.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a supplier", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                String sid = jTable3.getValueAt(r, 0).toString();
                String name = jTable3.getValueAt(r, 1).toString();
                String address = jTable3.getValueAt(r, 2).toString();
                String subject = jTable3.getValueAt(r, 3).toString();

                jTextField1.setText(name);
                jTextField4.setText(subject);
                jTextArea1.setText(address);

            }

        }
    }//GEN-LAST:event_jTable3MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r = jTable3.getSelectedRow();
        String tid = jTable3.getValueAt(r, 0).toString();

        try {
            ResultSet ssid = MySQL.search("SELECT * FROM `Teacher` WHERE `Tno` = '" + tid + "'");

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a Teacher", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                if (ssid.next()) {

                    String fn = jTextField1.getText();
                    String sub = jTextField4.getText();
                    String address = jTextArea1.getText();

                    if (fn.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Please select a Teacher", "Warning", JOptionPane.WARNING_MESSAGE);

                    } else if (sub.isEmpty()) {

                        JOptionPane.showMessageDialog(this, "Please select a Teacher", "Warning", JOptionPane.WARNING_MESSAGE);

                    } else if (address.isEmpty()) {

                        JOptionPane.showMessageDialog(this, "Please select a Teacher", "Warning", JOptionPane.WARNING_MESSAGE);

                    } else {

                        MySQL.iud("UPDATE `Teacher` SET `Name`= '" + fn + "',`subjects`= '" + sub + "',`Address`='" + address + "' WHERE `Tno`= '" + tid + "' ");

                        loadTeachers();
                        resetFields();

                        JOptionPane.showMessageDialog(this, "Teacher Updated", "Success", JOptionPane.INFORMATION_MESSAGE);

                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Please select a Teacher", "Warning", JOptionPane.WARNING_MESSAGE);

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

