    public void loadTeachers() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `Teacher` ORDER BY `Teacher`.`Tno` ASC ;");

            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("Tno"));
                v.add(rs.getString("Name"));
                v.add(rs.getString("Address"));
                v.add(rs.getString("subjects"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadTeachers(String text) {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `Teacher` WHERE `Teacher`.`Tno` LIKE '" + text + "%' OR `Teacher`.`Name` LIKE '" + text + "%' OR `Teacher`.`Address` LIKE '" + text + "%' OR `Teacher`.`subjects` LIKE '" + text + "%'  ORDER BY `Teacher`.`Tno` ASC ;");

            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("Tno"));
                v.add(rs.getString("Name"));
                v.add(rs.getString("Address"));
                v.add(rs.getString("subjects"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

